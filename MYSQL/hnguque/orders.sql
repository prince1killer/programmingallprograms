  AMOUNT FLOAT,
  ODATE DATE,
  CNUM INT,
  SNUM INT,
  FOREIGN KEY (CNUM) REFERENCES CUSTOMER(CNUM),
  FOREIGN KEY (SNUM) REFERENCES SALESMAN(SNUM)
);

